<h2><strong><center> DEUX PROJETS IMPORTANTS SONT EN COURS: <br> APPAICS ET ESPRIT D'ECHANGE.  </center> </strong> </h2>
<h3> 1- APPAICS (2022) </h3>
<p>
 <b> Objectifs </b> : <br>
- Permettre l'accès aux soins des personnes âgées en situation
d'isolement ; <br>
- Sensibiliser les habitants sur l'accès aux soins. <br><br>
<b> Descriptif :</b> </p> 
Accompagnement physique des personnes âgées isolées : prise de
RDV, accompagnement aller-retour du domicile de la personne au
centre de santé où elle a RDV.
Suivi des RDV avec les professionnels de santé.
Formation des accompagnateurs bénévoles : règles de conduite dans
l'accompagnement</p> <br>
<h3> 2- ESPRIT D'ECHANGE (2022) </h3> 
<p>
  <b> Objectifs </b> : <br>
  - Créer des temps d’échanges conviviaux entre les bénévoles de l’association et les séniors accompagnés <br>
  - Organiser des sorties culturelles dans Paris et ses alentours <br>
  <b> Descriptif : </b>  <br>
  Esprit d'échange est un espace de rencontre entre personnes âgées : échange, discussions, débats, autour d'un repas convivial.<br>
  Créé au mois de juin 2020, il a lieu actuellement le dernier vendredi de chaque mois, au centre social et culturel la Maison Bleue. Nous souhaitons développer l'activité en le proposant 2 fois par mois.
  Esprit d'échange permet également à ces personnes d'organiser et bénéficier de sorties culturelles (cinéma, théâtre, musée) : le programme est défini avec elles.
  Public cible : <br>
  - les bénévoles de l’association et les séniors accompagnés
   </p>
